package string_Classes;

public class StringBuilder_07 {

	/*
	 * stringbuilder is very similar to stringbuffer 
	 * 
	 */ 
	
	public static void main(String[] args) {
		
		StringBuilder sbr = new StringBuilder(" Computer");
		
		System.out.println("sbr is : " + sbr);
	 
		// with the append method we can add any data type. 
		 
		sbr.append(" MacBook");
		System.out.println(sbr);
		
		
		// need more example of StringBuilder 
		
	}		
}
