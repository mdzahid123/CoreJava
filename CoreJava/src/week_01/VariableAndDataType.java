package week_01;

public class VariableAndDataType {

	@SuppressWarnings("unused")
	private static boolean b;

	/*
	 * 
	 * only these 4 things can be used in variable name,
	 * 
	 * 1: A-Z, a-z 2: 0,1-9 3: _ underscore 4: $ dollar sign
	 * 
	 * variable can not be start with number, Space can't be use in variable name
	 * 
	 * 
	 */

	public static void main(String[] args) {

		char c;
		short s;
		int i;
		float f;
		double d;

		b = true;

	}

}
