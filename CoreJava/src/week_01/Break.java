package week_01;

public class Break {
	
	

	public static void main(String[] args) {
		
		
		for(int i = 1; i <= 100; i++) { // it will print 1-9 and break at 10, the rest number will not print. 
			
			if(i==10)  
			
				break;  // if we use continue statement then it will continue print 1-100 except 10. 
			
			
			System.out.println(i);
			
					
		}
		
		

	}

	}
